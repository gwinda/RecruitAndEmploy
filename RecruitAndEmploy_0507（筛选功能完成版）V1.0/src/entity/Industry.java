package entity;

public class Industry {
     int Id;//类型ID
     String name;//类型名称
    public Industry (){}
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
     
	
}
