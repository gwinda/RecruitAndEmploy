package domain;

public class Ecollection {
	private int id;
	private int eiid;
	private int resumeId;
	private String ename;
	private int eclass;
	private String time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEiid() {
		return eiid;
	}
	public void setEiid(int eiid) {
		this.eiid = eiid;
	}
	public int getResumeId() {
		return resumeId;
	}
	public void setResumeId(int resumeId) {
		this.resumeId = resumeId;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public int getEclass() {
		return eclass;
	}
	public void setEclass(int eclass) {
		this.eclass = eclass;
	}
	public String getTime() {
		return time;
	} 
	public void setTime(String time) {
		this.time = time;
	}
	
	
	

}
